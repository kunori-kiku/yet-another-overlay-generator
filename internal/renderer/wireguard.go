package renderer

import (
	"bytes"
	"fmt"
	"strings"
	"text/template"

	"github.com/kunorikiku/yet-another-overlay-generator/internal/compiler"
	"github.com/kunorikiku/yet-another-overlay-generator/internal/model"
)

// WireGuardConfig WireGuard 
type WireGuardConfig struct {
	// 
	NodeName   string
	PrivateKey string
	OverlayIP  string
	ListenPort int
	MTU        int

	// Peer 
	Peers []WireGuardPeer
}

// WireGuardPeer WireGuard peer 
type WireGuardPeer struct {
	// /
	Name string

	// 
	PublicKey string

	// AllowedIPs
	AllowedIPs string

	// Endpoint（）
	Endpoint string

	// PersistentKeepalive（，0 ）
	PersistentKeepalive int
}

const wgConfigTemplate = `# WireGuard configuration for {{ .NodeName }}
# Generated by Overlay Network Config Orchestrator

[Interface]
PrivateKey = {{ .PrivateKey }}
Address = {{ .OverlayIP }}/32
{{- if gt .ListenPort 0 }}
ListenPort = {{ .ListenPort }}
{{- end }}
{{- if gt .MTU 0 }}
MTU = {{ .MTU }}
{{- end }}
{{ range .Peers }}
# Peer: {{ .Name }}
[Peer]
PublicKey = {{ .PublicKey }}
AllowedIPs = {{ .AllowedIPs }}
{{- if .Endpoint }}
Endpoint = {{ .Endpoint }}
{{- end }}
{{- if gt .PersistentKeepalive 0 }}
PersistentKeepalive = {{ .PersistentKeepalive }}
{{- end }}
{{ end }}`

// RenderWireGuardConfig  WireGuard 
func RenderWireGuardConfig(node *model.Node, peers []compiler.PeerInfo, keys compiler.KeyPair) (string, error) {
	config := WireGuardConfig{
		NodeName:   node.Name,
		PrivateKey: keys.PrivateKey,
		OverlayIP:  node.OverlayIP,
		ListenPort: node.ListenPort,
		MTU:        node.MTU,
		Peers:      make([]WireGuardPeer, 0, len(peers)),
	}

	for _, p := range peers {
		wgPeer := WireGuardPeer{
			Name:                p.NodeName,
			PublicKey:           p.PublicKey,
			AllowedIPs:          strings.Join(p.AllowedIPs, ", "),
			Endpoint:            p.Endpoint,
			PersistentKeepalive: p.PersistentKeepalive,
		}
		config.Peers = append(config.Peers, wgPeer)
	}

	return renderTemplate("wg.conf", wgConfigTemplate, config)
}

// RenderAllWireGuardConfigs  WireGuard 
func RenderAllWireGuardConfigs(topo *model.Topology, peerMap map[string][]compiler.PeerInfo, keys map[string]compiler.KeyPair) (map[string]string, error) {
	configs := make(map[string]string)

	nodeMap := make(map[string]*model.Node)
	for i := range topo.Nodes {
		nodeMap[topo.Nodes[i].ID] = &topo.Nodes[i]
	}

	for nodeID, peers := range peerMap {
		node := nodeMap[nodeID]
		if node == nil {
			continue
		}

		key, ok := keys[nodeID]
		if !ok {
			return nil, fmt.Errorf(" %s ", nodeID)
		}

		config, err := RenderWireGuardConfig(node, peers, key)
		if err != nil {
			return nil, fmt.Errorf(" %s  WireGuard : %w", node.Name, err)
		}

		configs[nodeID] = config
	}

	return configs, nil
}

func renderTemplate(name, tmpl string, data interface{}) (string, error) {
	t, err := template.New(name).Parse(tmpl)
	if err != nil {
		return "", fmt.Errorf(": %w", err)
	}

	var buf bytes.Buffer
	if err := t.Execute(&buf, data); err != nil {
		return "", fmt.Errorf(": %w", err)
	}

	return buf.String(), nil
}
